class Student {
    constructor(id,first_name,last_name,birthdate, bio, class_name, age) {
        this.id = id;
        this.first_name = first_name
        this.last_name = last_name;
        this.birthdate = birthdate
        this.bio = bio
        this.class_name = class_name;
        this.age = age
    }
}
module.exports = Student;